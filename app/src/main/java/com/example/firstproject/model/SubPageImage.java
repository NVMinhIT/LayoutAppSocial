package com.example.firstproject.model;

public class SubPageImage {
    private int id, idImg;

    public SubPageImage(int id, int idImg) {
        this.id = id;
        this.idImg = idImg;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdImg() {
        return idImg;
    }

    public void setIdImg(int idImg) {
        this.idImg = idImg;
    }
}
